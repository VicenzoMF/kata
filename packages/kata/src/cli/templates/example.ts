// Templates for the runnable app that `kata init` scaffolds (issue #200).
//
// `kata init [dir]` produces a *complete, runnable* Kata app following the
// mandatory layout in AGENTS.md — `src/app.ts`, `src/context.ts`, a
// `src/middlewares/` folder, and `src/modules/<domain>/` modules each with the
// full `route` / `service` / `schema` / `test` / `hurl` five-file set. Two
// example modules ship so a newcomer sees the canonical GET *and* POST shapes:
//
//   • `health`   — GET /health, the smallest liveness probe.
//   • `greetings` — POST /greetings (validated body) + GET /greetings/:id
//                   (validated params, 404 on miss): the create/read pattern.
//
// This supersedes ADR-0015 D1, which scaffolded only a minimal two-file app
// behind `--with-example`; issue #200 makes the full app the default `kata init`
// output (`--minimal` keeps the old harness-only behaviour). See the new
// bootstrap ADR for the decision record.
//
// ── Template delivery (issue #200, open question) ────────────────────────────
// Templates are **inline strings compiled into `dist/`**, not a `templates/`
// directory shipped in the package. `package.json#files` lists only `dist`, so
// tsup bundles these strings into the CLI bundle and the published tarball stays
// a single `dist` tree — no second copy of the templates to ship, drift from, or
// add to `files`. The trade-off (templates are not browsable as standalone files
// in the tarball) is acceptable: they are plain exported constants here, fully
// readable in source, and the generated bytes are the locked contract asserted
// in `generators.test.ts`.
//
// All generated sources avoid backticks/`${}` (they use string concatenation
// instead), so each template below is a clean backtick literal needing no
// escaping — the same convention the original example templates used.

/** `src/context.ts` — the typed DI surface (ADR-0004); re-exports the bound factory. */
export const exampleContextSource = `import { defineContext } from 'katajs'

// The typed dependency-injection surface for this app (ADR-0004). It starts
// empty — register singleton(...) / scoped<T>() slots here as the app grows,
// then read them inside handlers and middleware with c.get('key').
export const k = defineContext({})

// Re-export the bound factory so every module inherits this app's exact types:
// c.get('key') only compiles for keys registered above.
export const { defineRoute, defineMiddleware, createApp } = k

export type AppRegistry = typeof k.registry
`

