// ── greetings module — POST /greetings + GET /greetings/:id (create/read) ────

/** `src/modules/greetings/greetings.schema.ts` — the greetings DTOs (ADR-0005). */
export const exampleGreetingsSchemaSource = `import { z } from 'zod'

// All Zod schemas for a domain live here, never inline in the route (ADR-0005) —
// the route imports them by name. Export the z.infer types alongside so a single
// import pulls both the runtime validator and the compile-time type.
export const CreateGreetingBodySchema = z.object({
  name: z.string().min(1),
})

export const GreetingParamsSchema = z.object({
  id: z.string(),
})

export const GreetingSchema = z.object({
  id: z.string(),
  message: z.string(),
})

export type CreateGreetingBody = z.infer<typeof CreateGreetingBodySchema>
export type Greeting = z.infer<typeof GreetingSchema>
`

/** `src/modules/greetings/greetings.service.ts` — pure logic + in-memory store. */
export const exampleGreetingsServiceSource = `import type { CreateGreetingBody, Greeting } from './greetings.schema'

// A module-local store stands in for a database — swap it for a real one behind
// the same two functions. Register the db handle as a singleton in context.ts
// (ADR-0004) and read it via c.get('db') when you do.
const greetings = new Map<string, Greeting>()

export function createGreeting(input: CreateGreetingBody): Greeting {
  const id = crypto.randomUUID()
  const greeting: Greeting = { id, message: 'Hello, ' + input.name + '!' }
  greetings.set(id, greeting)
  return greeting
}

export function getGreeting(id: string): Greeting | null {
  return greetings.get(id) ?? null
}
`

/** `src/modules/greetings/greetings.route.ts` — POST + GET, body + params + 404. */
export const exampleGreetingsRouteSource = `import { defineRoute } from '../../context'

import { CreateGreetingBodySchema, GreetingParamsSchema, GreetingSchema } from './greetings.schema'
import { createGreeting, getGreeting } from './greetings.service'

// POST /greetings — validates the JSON body against CreateGreetingBodySchema
// (ADR-0003) before the handler runs, then returns the created greeting.
export const createGreetingRoute = defineRoute({
  method: 'POST',
  path: '/greetings',
  input: { body: CreateGreetingBodySchema },
  output: GreetingSchema,
  handler: (c) => createGreeting(c.input.body),
})

// GET /greetings/:id — validates the path param, then reads the greeting back.
// A miss returns the unified ADR-0008 error envelope with a 404.
export const getGreetingRoute = defineRoute({
  method: 'GET',
  path: '/greetings/:id',
  input: { params: GreetingParamsSchema },
  output: GreetingSchema,
  handler: (c) => {
    const greeting = getGreeting(c.input.params.id)
    if (!greeting) return c.error('not_found', 'Greeting not found', { status: 404 })
    return greeting
  },
})
`

/** `src/modules/greetings/greetings.test.ts` — unit tests for the service. */
export const exampleGreetingsTestSource = `import { describe, expect, it } from 'vitest'

import { createGreeting, getGreeting } from './greetings.service'

describe('greetings.service', () => {
  it('creates a greeting with a generated id and a message', () => {
    const greeting = createGreeting({ name: 'Ada' })
    expect(greeting.id.length).toBeGreaterThan(0)
    expect(greeting.message).toBe('Hello, Ada!')
  })

  it('reads a created greeting back by id', () => {
    const created = createGreeting({ name: 'Linus' })
    expect(getGreeting(created.id)).toEqual(created)
  })

  it('returns null for an unknown id', () => {
    expect(getGreeting('does-not-exist')).toBeNull()
  })
})
`

